package modelo;

public class Estrella {
	  private String id_estrella ;
	  private String nombre ;
	  private String  id_sistema_planetario;
	  
	  
	public String getId_estrella() {
		return id_estrella;
	}
	public void setId_estrella(String id_estrella) {
		this.id_estrella = id_estrella;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getId_sistema_planetario() {
		return id_sistema_planetario;
	}
	public void setId_sistema_planetario(String id_sistema_planetario) {
		this.id_sistema_planetario = id_sistema_planetario;
	}
	  
	  

}
